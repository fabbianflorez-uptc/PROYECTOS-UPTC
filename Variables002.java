public class Variables002 {
    public static void main(String[] args) {
        
        // int = Integer (Entero). Solo numeros sin decimales.
        int edad = 19; 
        
        // double = Numeros con coma o punto decimal. Se usa para estaturas o precios.
        double estatura = 1.64; 
        
        // String = Cadena de caracteres (Texto). Siempre con S mayuscula y comillas dobles "".
        String ciudad = "Soatá"; 
        
        // char = Character (Caracter). Solo una letra o simbolo, usa comillas simples ''.
        char inicial = 'F'; 
        
        // boolean = Solo tiene dos estados: verdad (true) o mentira (false).
        boolean esIngeniero = false; 

        // El simbolo + sirve para "concatenar", osea, pegar el texto con la bariable.
        System.out.println("Mi inicial es " + inicial + " y vivo en " + ciudad);
    }
}
